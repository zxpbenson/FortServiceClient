package com.fortappend.fortservice.client.vo;

public class BaseResourceVo {

    private String personAccount;
    private String resourceIp;
    private String resourceAccount;

    public String getPersonAccount() {
        return personAccount;
    }

    public void setPersonAccount(String personAccount) {
        this.personAccount = personAccount;
    }

    public String getResourceIp() {
        return resourceIp;
    }

    public void setResourceIp(String resourceIp) {
        this.resourceIp = resourceIp;
    }

    public String getResourceAccount() {
        return resourceAccount;
    }

    public void setResourceAccount(String resourceAccount) {
        this.resourceAccount = resourceAccount;
    }

}
